<?php
  $locations = array();
  $args = array(
    'post_type' => 'project_location',
    'posts_per_page' => -1,
  );
  $loop = new WP_Query( $args );
  while ( $loop->have_posts() ) {
    $loop->the_post();
    $locations[] = array(
      'id' => get_the_ID(),
      'name' => get_field('name'),
      'description' => get_field('description'),
      'latitude' => get_field('latitude'),
      'longitude' => get_field('longitude'),
    );
  }
  wp_reset_query();
